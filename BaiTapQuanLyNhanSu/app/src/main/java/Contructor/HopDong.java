package Contructor;

public class HopDong {
    String mahd;
    String mall;
    String maloaihd;
    String ngaycapluong;
    String ngayki;
    String nguoiki;
    String apdungtungay;
    String dieukhoan;
    String ghichu;

    public HopDong(String mahd, String mall, String maloaihd, String ngaycapluong, String ngayki, String nguoiki, String apdungtungay, String dieukhoan, String ghichu) {
        this.mahd = mahd;
        this.mall = mall;
        this.maloaihd = maloaihd;
        this.ngaycapluong = ngaycapluong;
        this.ngayki = ngayki;
        this.nguoiki = nguoiki;
        this.apdungtungay = apdungtungay;
        this.dieukhoan = dieukhoan;
        this.ghichu = ghichu;
    }

    public String getMaHD() {
        return mahd;
    }

    public void setMaHD(String mahd) {
        this.mahd = mahd;
    }

    public String getMaLL() {
        return mall;
    }

    public void setMaLL(String mall) {
        this.mall = mall;
    }

    public String getMaLoaiHD() {
        return maloaihd;
    }

    public void setMaLoaiHD(String maloaihd) {
        this.maloaihd = maloaihd;
    }

    public String getNgayCapLuong() {
        return ngaycapluong;
    }

    public void setNgayCapLuong(String ngaycapluong) {
        this.ngaycapluong = ngaycapluong;
    }
    public String getNgayKi() {
        return ngayki;
    }

    public void setNgayKi(String ngayki) {
        this.ngayki = ngayki;
    }
    public String getNguoiKi() {
        return nguoiki;
    }

    public void setNguoiKi(String nguoiki) {
        this.nguoiki = nguoiki;
    }
    public String getApDungTuNgay() {
        return apdungtungay;
    }

    public void setApDungTuNgay(String apdungtungay) {
        this.apdungtungay = apdungtungay;
    }
    public String getDieuKhoan() {
        return dieukhoan;
    }

    public void setDieuKhoan(String dieukhoan) {
        this.dieukhoan = dieukhoan;
    }
    public String getGhiChu() {
        return ghichu;
    }

    public void setGhiChu(String ghichu) {
        this.ghichu = ghichu;
    }
}

